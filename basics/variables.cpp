#include <iostream>
using namespace std;

int main(){
    int age = 20;
    cout << age << endl ;
    // sizeof() operator is used to print size of a variable in 'bytes'.
    cout << sizeof(age) << endl;
    char grade = 'A';
    cout << grade << endl;
    float PI = 3.14f;
    cout << PI << endl;
    bool issafe = true;
    cout << issafe << endl;
    double price = 100.99;
    cout << price << endl;
    return 0;
}